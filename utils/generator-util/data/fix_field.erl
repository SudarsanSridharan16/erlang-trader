-module(fix_field).

-export([create/2,
          is_defined/1,
         is_valid/1]).

-include("fix.hrl").
-include("fix_field.hrl").

%% ====================================================================
%% API functions
%% ====================================================================

-spec create(Id, V) -> {fix_field()} when
        Id :: integer(),
        V :: any(). 
create(Id, V) -> 
    FD = create_field_descriptor(Id),
	   case is_valid_field_descriptor(FD) of 
	       false -> 
	            #fix_field{};
	       true -> 
	            Fun = FD#fix_field_descriptor.tranform,
	            #fix_field{id = Id, value = Fun(V), descriptor = FD}
    end.

-spec is_valid(F) -> boolean() when
        F :: fix_field().
is_valid(F) -> 
    is_defined_value(F#fix_field.id),
    is_defined_value(F#fix_field.value),
    is_defined_value(F#fix_field.descriptor).

-spec is_defined(Id) -> boolean() when
        Id :: integer().
is_defined(Id) ->
    FD = create_field_descriptor(Id),
    is_valid_field_descriptor(FD).

%% ====================================================================
%% Internal functions
%% ====================================================================

-spec is_valid_field_descriptor(FD) -> boolean() when
        FD :: fix_field_descriptor().
is_valid_field_descriptor(FD) -> 
    is_defined_value(FD#fix_field_descriptor.name),
    is_defined_value(FD#fix_field_descriptor.xml_tag),
    is_defined_value(FD#fix_field_descriptor.tranform),
    is_defined_value(FD#fix_field_descriptor.version),
    is_defined_value(FD#fix_field_descriptor.accepted_values).

